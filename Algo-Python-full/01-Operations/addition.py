"""
Module contenant les opérations arithmétiques de base. <---- Docstring
"""

def addition(a : int, b : int) -> int:  # <---- Fonction a et b son les Paramètres de la fonction
    return a + b  # <---- Valeur  renvoyé par la fonction


def quotient(a, b):
    return a // b


def modulo(a, b):
    return a % b